import indexAjaxData from '../../ajax/indexAjaxData';
import htmlResult from './htmlResult';

export default function senditems(user, invIdAry) {
  return indexAjaxData({
    cmd: 'trade',
    subcmd: 'senditems',
    xc: window.ajaxXC,
    target_username: user,
    sendItemList: invIdAry,
  }).then(htmlResult);
}
